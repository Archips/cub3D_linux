#include "libft.h"

/*
 ** @brief      Check for an alphanumeric character.
 **
 ** @see        ISALNUM(3) <ctype.h>
 */

int	ft_isalnum(int c)
{
	return ((c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9'));
}
