#include "libft.h"

/*
 ** @brief      Checks for an alphabetic character.
 **
 ** @see        ISALPHA(3) <ctype.h>
 */

int	ft_isalpha(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'));
}
